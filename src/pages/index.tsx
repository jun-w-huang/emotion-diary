const Home: NextPage = () => {
  const user = useUser();

  const { data } = api.emotionEvent.getAll.useQuery();

  const localizer = dayjsLocalizer(dayjs);

  const myEventsList = data?.map((emotionEvent) => {
    return {
      id: emotionEvent.id,
      title: emotionEvent.title,
      start: emotionEvent.start,
      end: emotionEvent.end,
    }

  });

  return (
    <>
      <Head>
        <title>Emotion Diary</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center">
        <div>
          <Calendar
            localizer={localizer}
            onSelectEvent={(event) => {
            }}
            events={myEventsList}
            startAccessor="start"
            endAccessor="end"
            style={{ height: 500 }}
          />
        </div>

        {user.isSignedIn ? <SignOutButton /> : <SignInButton />}

        <div>
          {data?.map((emotionEvent) => (
            <div key={emotionEvent.id}>{emotionEvent.title}</div>
          ))}
        </div>
      </main>
    </>
  );
};

export default Home;
